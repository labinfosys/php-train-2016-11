<?php

$arr = array (
'А' => array ('Абакан', 'Абдулино', 'Агинское'),
'Б' => array ('Барнаул', 'Белгород',),
'В' => array ('Великий Новгород', 'Владивосток', 'Владикавказ', 'Владимир', 'Волгоград', 'Вологда', 'Воронеж')
);

$dl = '';
foreach ($arr as $key => $value1) {
	$dl .= '<dt>' . $key . '</dt>';
	foreach ($value1 as $value2) {
		$dl .= '<dd>' . $value2 . '</dd>';
	}
}
$dl = '<dl>' . $dl . '</dl>';

echo $dl;